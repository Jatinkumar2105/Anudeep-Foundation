# Check if a number is within a certain range (10 to 20)
# Input a number from the user
num = float(input("Enter a number: "))

# Check if the number is within the range of 10 to 20 (inclusive)
if 10 <= num <= 20:
    print("The number is within the range of 10 to 20.")
else:
    print("The number is outside the range of 10 to 20.")
