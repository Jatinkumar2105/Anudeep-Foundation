# Check if the sum of two numbers is greater than 50.
# Input two numbers from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Calculate the sum of the two numbers
sum_of_numbers = num1 + num2

# Check if the sum is greater than 50
if sum_of_numbers > 50:
    print("The sum of the two numbers is greater than 50.")
else:
    print("The sum of the two numbers is not greater than 50.")
